# If Statements in Python (2025)

# ───────────────────────────────
# (1) BASIC IF STATEMENT
number = 10
if number > 0:
    print("The number is positive")

print("-" * 50)

# ───────────────────────────────
# (2) IF STATEMENT WITH STRING
name = "Alice"
if name == "Alice":
    print("Hello, Alice!")

