# Built-in Functions in Python (2025)

# (1) USING len() TO COUNT CHARACTERS
message = "Python"
print("The length is:", len(message))

# (2) USING max() AND min()
numbers = [3, 7, 1, 9]
print("Maximum is:", max(numbers))
print("Minimum is:", min(numbers))

# (3) USING type() TO CHECK DATA TYPE
value = 42
print("Type of value:", type(value))
