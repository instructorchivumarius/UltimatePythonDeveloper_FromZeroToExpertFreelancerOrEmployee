# If – Else in Python (2025)

# ───────────────────────────────
# (1) BASIC IF – ELSE
number = -5
if number >= 0:
    print("The number is positive")
else:
    print("The number is negative")

print("-" * 50)

# ───────────────────────────────
# (2) IF – ELSE WITH STRING
user = "guest"
if user == "admin":
    print("Welcome, Admin!")
else:
    print("Access limited for guests")
