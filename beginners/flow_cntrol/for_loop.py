# For Loops in Python (2025)

# ───────────────────────────────
# (1) FOR LOOP with LIST
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("Fruit:", fruit)

print("-" * 50)

# ───────────────────────────────
# (2) FOR LOOP with STRING
text = "Python"
for char in text:
    print("Char:", char)
