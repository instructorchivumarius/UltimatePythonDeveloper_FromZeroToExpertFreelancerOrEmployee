# Data types: integers and floats

# Integers
age = 30
birth_year = 2025 - age
print("Year of birth is:", birth_year)

# Floats
temperature = 36.6
weight = 72.5
print("Body temperature is:", temperature)
print("Weight is:", weight)

# Basic arithmetic
sum_result = 10 + 5
difference = 10 - 3
product = 4 * 2
quotient = 10 / 3       # Division returns float
remainder = 10 % 3      # Modulo
power = 2 ** 3          # Exponentiation

print("Sum:", sum_result)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
print("Remainder:", remainder)
print("Power:", power)

# Type casting
x = "10"
y = int(x) + 5
print("Result is:", y)

z = float("3.14")
print("Converted number:", z)
